    // get gas staions that belongs to selected company
    public ResultSet getResultFromGasStations() {
        ResultSet rs = null;

        String companiesText = (String) jComboBox1.getSelectedItem();

        // if there is no companies in list, than we can't select
        // gas stations and result set is null
        if (companiesText != null) {

            // get company_id from companiesComboBox
            String companyId
                = (companiesText).split(":")[0];
            System.out.println("companyId: " + companyId);

            try {
                rs = stmt.executeQuery(
                    "SELECT * FROM Gas_stations "
                    + "WHERE company_id = " + quotate(companyId));

            } catch (SQLException e) {
                e.printStackTrace();
            }
        }

        return rs;
    }

    public String quotate(String content) {
        return "'" + content + "'";
    }

