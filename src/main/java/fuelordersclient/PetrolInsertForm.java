    public ResultSet getResultFromPetrolTables() {

        String allTablesQuery
            = "SELECT 	Companies.name, "
            + "         Gas_stations.code, "
            + "         Gas_stations.address, "
            + "         Orders.date, "
            + "         Orders.gas_type, "
            + "         Orders.cost_per_liter, "
            + "         Orders.discount, "
            + "         Orders.amount_liters, "
            + "         Orders.total_cost "
            + "FROM 	Companies, Gas_stations, Orders "
            + "WHERE 	Orders.gas_station_id = Gas_stations.id"
            + "     AND Gas_stations.company_id = Companies.id "
            + "ORDER BY Orders.id;";

        ResultSet rs = null;

        try {
            rs = stmt.executeQuery(allTablesQuery);

        } catch (SQLException e) {
            e.printStackTrace();
        }

        return rs;
    }

